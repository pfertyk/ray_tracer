class Material:
    def __init__(self, ambientColor = [255, 255, 255], diffuseColor = [255, 255, 255], specularColor = [255, 255, 255], ambientCoeff = 1.0, diffuseCoeff = 1.0, specularCoeff = 1.0, exponent = 27):
            self.ambientColor = ambientColor
            self.diffuseColor = diffuseColor
            self.specularColor = specularColor
            self.ambientCoeff = ambientCoeff
            self.diffuseCoeff = diffuseCoeff
            self.specularCoeff = specularCoeff
            self.exponent = exponent
        
        